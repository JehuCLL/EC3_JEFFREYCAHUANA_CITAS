package com.example.com.dto;

public class ClienteResponseDTo {

	private int idClienteDTO;
	private String nombreDTO;
	private Integer celularDTO;
	
	public int getIdClienteDTO() {
		return idClienteDTO;
	}
	
	public void setIdClienteDTO(int idClienteDTO) {
		this.idClienteDTO = idClienteDTO;
	}
	
	public String getNombreDTO() {
		return nombreDTO;
	}
	
	public void setNombreDTO(String nombreDTO) {
		this.nombreDTO = nombreDTO;
	}
	
	public Integer getCelularDTO() {
		return celularDTO;
	}
	
	public void setCelularDTO(Integer celularDTO) {
		this.celularDTO = celularDTO;
	}

}
